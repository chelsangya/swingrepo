    private void billingButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_billingButtonActionPerformed
        // TODO add your handling code here:
     JFileChooser fileChooser = new JFileChooser();
    fileChooser.setDialogTitle("Select Profile Picture");
    fileChooser.setFileFilter(new javax.swing.filechooser.FileNameExtensionFilter("Images", "jpg", "jpeg", "png"));

    int returnValue = fileChooser.showOpenDialog(null);
    
    if (returnValue == JFileChooser.APPROVE_OPTION) {
        File selectedFile = fileChooser.getSelectedFile();
        
        try {
            BufferedImage originalImage = ImageIO.read(selectedFile);
            if (originalImage != null) {
                // Scale the image properly
                BufferedImage resizedImage = new BufferedImage(100, 100, BufferedImage.TYPE_INT_ARGB);
                Graphics2D g2d = resizedImage.createGraphics();
                g2d.drawImage(originalImage, 0, 0, 100, 100, null);
                g2d.dispose();
                
                // Set the resized image as profile icon
                ImageIcon profileIcon1 = new ImageIcon(resizedImage);
                titleLabel1.setIcon(profileIcon1);
                
                // Convert image to byte array (if needed for database storage)
                ByteArrayOutputStream baos = new ByteArrayOutputStream();
                ImageIO.write(resizedImage, "png", baos);
                image = baos.toByteArray();
                 UpdateProfilePictureModel pm = new UpdateProfilePictureModel(uid,image);
                  AuthDAO auth = new AuthDAO();
                    AuthData userm;
                try {
             userm = auth.updateProfilePicture(pm,uid);
              this.user=userm;
         } catch (SQLException ex) {
             Logger.getLogger(PasswordChangeView.class.getName()).log(Level.SEVERE, null, ex);
         }                 
                System.out.println("Profile picture updated successfully.");
            } else {
                System.out.println("Invalid image file.");
            }
        } catch (IOException ex) {
            System.out.println("Error loading image: " + ex.getMessage());
        }
    }
    }//GEN-LAST:event_billingButtonActionPerformed

