    private void changePasswordButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changePasswordButtonActionPerformed

        // TODO add your handling code here:
        String currentPassword=currentPasswordTextField.getText();
        String newPassword=newPasswordTextField.getText();
        String confirmPassword= confirmPasswordTextField.getText();
        if(currentPassword.isEmpty() || "".equals(currentPassword)||newPassword.isEmpty() || "".equals(newPassword)||confirmPassword.isEmpty() || "".equals(confirmPassword)){
            JOptionPane.showMessageDialog(this, "Please enter all the fields");
            return;
        }
        if (currentPassword.equals(this.password)){
            if(newPassword.equals(confirmPassword)){
                AuthDAO auth= new AuthDAO();
                UpdatePasswordModel profile = new UpdatePasswordModel(uid,newPassword); 
                try {
                    auth.updatePassword(profile, uid);
                    
                } catch (SQLException ex) {
                    Logger.getLogger(PasswordChangeView.class.getName()).log(Level.SEVERE, null, ex);
                }
                
            }else{
                 JOptionPane.showMessageDialog(this, "New Password and Confirm Password do not match");
            return;
            }
        } else{
            JOptionPane.showMessageDialog(this, "Current Password is not correct");
            return;
        }
       
        UpdatePasswordModel lm = new UpdatePasswordModel(uid,newPassword);
        AuthDAO auth = new AuthDAO();
        AuthData userm;
         try {
             userm = auth.updatePassword(lm,uid);
                     this.user=userm;
                     

        JOptionPane.showMessageDialog(this, "Profile Updated Successfully");
         } catch (SQLException ex) {
             Logger.getLogger(PasswordChangeView.class.getName()).log(Level.SEVERE, null, ex);
         }


    }//GEN-LAST:event_changePasswordButtonActionPerformed

